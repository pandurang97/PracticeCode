package Day2;

public class SwitchcaseStatement {

	public static void main(String[] args) {

		int num =20;
		
		switch(num) {
		case 3: System.out.println(num);
		break;
		case 10: System.out.println(num);
		break;
		case 20: System.out.println(num);
		break;
		case 12: System.out.println(num);
	break;
		default: System.out.println("Default");
		}
	}

}
